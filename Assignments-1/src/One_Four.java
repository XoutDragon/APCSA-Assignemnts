public class One_Four {
    public static void main(String args[]) {
        // On one line
        System.out.println("Knowledge is power");

        // On three lines, one word per line, with words centered relative to each other
        System.out.println("Knowledge\n   is\n  power");

        // Inside a box made up of the characters = and |
        System.out.print("===========\n" +
                         "|Knowledge|\n" +
                         "|   is    |\n" +
                         "|  power  |\n" +
                         "===========");
    }
}
